public class Variable {
    public static void main(String[] args) {
        String name = "Aditya";
        System.out.println(name);
        int myNum = 69;
        System.out.println(myNum);
        String fName;
        fName = "Aditya";
        System.out.println(fName);
        name = "bittu";
        System.out.println(name);
        final int cons = 6;
        // cons = 8;
        System.out.println(cons);
        // More Variable types.
        int myNumb = 65;
        float myFloatNum = 5.99f;
        String myString = "MyString";
        char myChar = 'd';
        boolean myBool = true;
    }
}
